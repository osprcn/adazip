package org.zip.adazip;

public class Partition {
    private double size;
    private SizeFormat format;

    public double getSize() {
        return size;
    }

    public void setSize(double size) {
        this.size = size;
    }

    public SizeFormat getFormat() {
        return format;
    }

    public void setFormat(SizeFormat format) {
        this.format = format;
    }
}
